// ─── Blue-Green Zero-Downtime Deployment ─────────────────────────

interface BlueGreenConfig {
  activePort: number
  standbyPort: number
  healthCheckPath: string
  healthCheckInterval: number
  healthCheckTimeout: number
  unhealthyThreshold: number
  nginxConfigPath: string
}

const DEFAULT_BLUE_GREEN: BlueGreenConfig = {
  activePort: 3000,
  standbyPort: 3001,
  healthCheckPath: '/health',
  healthCheckInterval: 2000,
  healthCheckTimeout: 5000,
  unhealthyThreshold: 3,
  nginxConfigPath: '/etc/nginx/sites-enabled/speexjs',
}

function generateBlueGreenNginxConfig(domain: string, activePort: number, standbyPort: number): string {
  return `# Auto-generated by SpeexJS Blue-Green Deploy
# ${new Date().toISOString()}

upstream speexjs_active {
    server 127.0.0.1:${activePort};
}

upstream speexjs_standby {
    server 127.0.0.1:${standbyPort};
}

server {
    listen 80;
    server_name ${domain};

    location / {
        proxy_pass http://speexjs_active;
        proxy_http_version 1.1;
        proxy_set_header Upgrade $http_upgrade;
        proxy_set_header Connection 'upgrade';
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto $scheme;
        proxy_cache_bypass $http_upgrade;
    }

    location /health {
        proxy_pass http://speexjs_active;
        access_log off;
        return 200 "healthy\\n";
    }
}
`
}

function generateSwapScript(): string {
  return String.raw`#!/bin/bash
# Auto-generated by SpeexJS Blue-Green Deploy
set -e

ACTIVE_PORT=$1
STANDBY_PORT=$2
NGINX_CONFIG=$3

echo "🔄 Blue-Green: Switching traffic from :$ACTIVE_PORT to :$STANDBY_PORT..."

sed -i "s/:$ACTIVE_PORT/:$STANDBY_PORT/g" "$NGINX_CONFIG"
nginx -t || { echo "❌ Nginx config test failed"; exit 1; }
nginx -s reload

echo "✅ Traffic switched to :$STANDBY_PORT"
echo "ℹ️  Old instance on :$ACTIVE_PORT will be stopped after drain"
`
}

function generateHealthCheckScript(): string {
  const D = '$'
  return String.raw`#!/bin/bash
# Auto-generated by SpeexJS Health Check

URL=${D}1
TIMEOUT=${D}{2:-5}
INTERVAL=${D}{3:-2}
THRESHOLD=${D}{4:-3}

echo "🔍 Health checking $URL..."

for i in $(seq 1 $THRESHOLD); do
  STATUS=$(curl -s -o /dev/null -w "%{http_code}" --max-time $TIMEOUT $URL 2>/dev/null || echo "000")
  if [ "$STATUS" = "200" ]; then
    echo "   ✅ Attempt $i: Health check passed ($STATUS)"
    exit 0
  fi
  echo "   ⚠️  Attempt $i: Health check returned $STATUS, retrying in ${D}{INTERVAL}s..."
  sleep $INTERVAL
done

echo "❌ Health check failed after $THRESHOLD attempts"
exit 1
`
}

