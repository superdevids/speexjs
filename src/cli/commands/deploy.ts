// ─── Blue-Green Deploy ──────────────────────────────────────────

async function deployBlueGreen(options: DeployOptions): Promise<void> {
  const baseDir = resolve(process.cwd())
  const activePort = options.activePort ?? DEFAULT_BLUE_GREEN.activePort
  const standbyPort = options.standbyPort ?? DEFAULT_BLUE_GREEN.standbyPort
  const domain = options.domain ?? 'localhost'

  console.log(`\n  ${colors.bold('🌊 Blue-Green Deployment')}`)
  console.log(`  ${colors.dim('─'.repeat(50))}`)
  console.log(`  Active port:  ${colors.cyan(String(activePort))}`)
  console.log(`  Standby port: ${colors.cyan(String(standbyPort))}`)
  console.log(`  Domain:       ${colors.cyan(domain)}`)
  console.log()

  // Generate nginx config
  console.log(`  ${colors.cyan('→')} Generating nginx configuration...`)
  const nginxConfig = generateBlueGreenNginxConfig(domain, activePort, standbyPort)
  const deployDir = resolve(baseDir, 'deploy')
  if (!existsSync(deployDir)) {
    writeFileSync(resolve(deployDir, '.gitkeep'), '', 'utf-8')
  }
  writeFileSync(resolve(deployDir, 'nginx-blue-green.conf'), nginxConfig, 'utf-8')
  console.log(`  ${colors.green('✅')} deploy/nginx-blue-green.conf`)

  // Generate swap script
  const swapScript = generateSwapScript()
  writeFileSync(resolve(deployDir, 'swap.sh'), swapScript, 'utf-8')
  console.log(`  ${colors.green('✅')} deploy/swap.sh`)

  // Generate health check script
  const healthScript = generateHealthCheckScript()
  writeFileSync(resolve(deployDir, 'health-check.sh'), healthScript, 'utf-8')
  console.log(`  ${colors.green('✅')} deploy/health-check.sh`)

  console.log()
  console.log(`  ${colors.bold('📋 Deployment Steps:')}`)
  console.log(`  ${colors.cyan('1.')} Build: npm run build`)
  console.log(`  ${colors.cyan('2.')} Start standby: PORT=${standbyPort} node dist/index.js &`)
  console.log(`  ${colors.cyan('3.')} Health check: bash deploy/health-check.sh http://localhost:${standbyPort}/health`)
  console.log(`  ${colors.cyan('4.')} Swap: bash deploy/swap.sh ${activePort} ${standbyPort} ${nginxConfig}`)
  console.log(`  ${colors.cyan('5.')} Rollback: speexjs deploy --rollback`)
  console.log()
  console.log(`  ${colors.green('✓')} Blue-green configuration complete`)
}

async function rollbackDeploy(options: DeployOptions): Promise<void> {
  const activePort = options.activePort ?? DEFAULT_BLUE_GREEN.activePort
  const standbyPort = options.standbyPort ?? DEFAULT_BLUE_GREEN.standbyPort

  console.log(`\n  ${colors.bold('↩️  Rolling Back Deployment')}`)
  console.log(`  ${colors.dim('─'.repeat(50))}`)
  console.log(`  ${colors.cyan('→')} Switching traffic back to :${activePort}...`)
  console.log()
  console.log(`  ${colors.dim(`  Run: sed -i 's/:${standbyPort}/:${activePort}/g' deploy/nginx-blue-green.conf`)}`)
  console.log(`  ${colors.dim('  Run: nginx -t && nginx -s reload')}`)
  console.log()
  console.log(`  ${colors.green('✓')} Rollback initiated`)
}

// ─── Main Deploy Function ───────────────────────────────────────

export async function deploy(options?: Partial<DeployOptions>): Promise<void> {
  const opts: DeployOptions = {
    docker: options?.docker ?? false,
    vercel: options?.vercel ?? false,
    railway: options?.railway ?? false,
    render: options?.render ?? false,
    flyio: options?.flyio ?? false,
    blueGreen: options?.blueGreen ?? false,
    rollback: options?.rollback ?? false,
    init: options?.init ?? false,
    domain: options?.domain,
    activePort: options?.activePort,
    standbyPort: options?.standbyPort,
  }

  // Handle rollback early
  if (opts.rollback) {
    await rollbackDeploy(opts)
    return
  }

  // Handle blue-green
  if (opts.blueGreen) {
    await deployBlueGreen(opts)
    return
  }

  // Find target
  let target = ''
  for (const t of TARGETS) {
    if (t === 'blue-green') continue // handled above
    if (opts[t]) {
      target = t
      break
    }
  }

  if (!target) {
    console.log(`  ${colors.cyan('?')} Select deployment target:`)
    TARGETS.filter(t => t !== 'blue-green').forEach((t, i) => {
      console.log(`  ${colors.dim(`${i + 1})`)} ${t.charAt(0).toUpperCase() + t.slice(1)}`)
    })
    console.log()
    process.stdout.write(`  ${colors.cyan('→')} Enter number (1-${TARGETS.length - 1}): `)

    target = await new Promise<string>((resolve) => {
      process.stdin.once('data', (data) => {
        const input = data.toString().trim()
        const idx = Number.parseInt(input, 10) - 1
        const filtered = TARGETS.filter(t => t !== 'blue-green')
        if (idx >= 0 && idx < filtered.length) {
          resolve(filtered[idx]!)
        } else {
          console.log(`  ${colors.red('Invalid choice, defaulting to Docker')}`)
          resolve('docker')
        }
      })
    })
  }

  const baseDir = resolve(process.cwd())

  if (target === 'docker') {
    const dockerfilePath = resolve(baseDir, 'Dockerfile')
    if (!existsSync(dockerfilePath) || opts.init) {
      writeFileSync(dockerfilePath, generateDockerfile(), 'utf-8')
      console.log(`  ${colors.green('✅')} Dockerfile created`)
    } else {
      console.log(`  ${colors.yellow('⚠')} Dockerfile already exists`)
    }

    const composePath = resolve(baseDir, 'docker-compose.yml')
    if (!existsSync(composePath) || opts.init) {
      writeFileSync(composePath, generateDockerCompose(), 'utf-8')
      console.log(`  ${colors.green('✅')} docker-compose.yml created`)
    } else {
      console.log(`  ${colors.yellow('⚠')} docker-compose.yml already exists`)
    }

    if (!opts.init) printDeployInstructions('docker')
    console.log(`  ${colors.green('✓')} Docker setup complete`)
  }

  if (target === 'vercel') {
    const vercelJsonPath = resolve(baseDir, 'vercel.json')
    if (!existsSync(vercelJsonPath) || opts.init) {
      writeFileSync(vercelJsonPath, generateVercelJson(), 'utf-8')
      console.log(`  ${colors.green('✅')} vercel.json created`)
    } else {
      console.log(`  ${colors.yellow('⚠')} vercel.json already exists`)
    }
    if (!opts.init) printDeployInstructions('vercel')
    console.log(`  ${colors.green('✓')} Vercel setup complete`)
  }

  if (target === 'railway') {
    const railwayPath = resolve(baseDir, 'railway.json')
    if (!existsSync(railwayPath) || opts.init) {
      writeFileSync(railwayPath, generateRailwayJson(), 'utf-8')
      console.log(`  ${colors.green('✅')} railway.json created`)
    } else {
      console.log(`  ${colors.yellow('⚠')} railway.json already exists`)
    }
    if (!opts.init) printDeployInstructions('railway')
    console.log(`  ${colors.green('✓')} Railway setup complete`)
  }

  if (target === 'render') {
    const renderPath = resolve(baseDir, 'render.yaml')
    if (!existsSync(renderPath) || opts.init) {
      writeFileSync(renderPath, generateRenderYaml(), 'utf-8')
      console.log(`  ${colors.green('✅')} render.yaml created`)
    } else {
      console.log(`  ${colors.yellow('⚠')} render.yaml already exists`)
    }
    if (!opts.init) printDeployInstructions('render')
    console.log(`  ${colors.green('✓')} Render setup complete`)
  }

  if (target === 'flyio') {
    const flyPath = resolve(baseDir, 'fly.toml')
    if (!existsSync(flyPath) || opts.init) {
      writeFileSync(flyPath, generateFlyToml(), 'utf-8')
      console.log(`  ${colors.green('✅')} fly.toml created`)
    } else {
      console.log(`  ${colors.yellow('⚠')} fly.toml already exists`)
    }
    if (!opts.init) printDeployInstructions('flyio')
    console.log(`  ${colors.green('✓')} Fly.io setup complete`)
  }
}

export async function deployWithBlueGreen(options: DeployOptions): Promise<void> {
  await deployBlueGreen(options)
}
